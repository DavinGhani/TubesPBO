    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String questionId = inputQuestionId.getText();
        String name = inputQuestion.getText();
        String opt1 = inputOption1.getText();
        String opt2 = inputOption2.getText();
        String opt3 = inputOption3.getText();
        String opt4 = inputOption4.getText();
        String answer = inputAnswer.getText();
        
        try {
            Connection Con = db_quizzapp.konfigDB();
            PreparedStatement ps = Con.prepareStatement("UPDATE questions SET name = ?, opt1 = ?, opt2 = ?, opt3 = ?, opt4 = ?, answer = ? WHERE id = ?");
            ps.setString(1, name);
            ps.setString(2, opt1);
            ps.setString(3, opt2);
            ps.setString(4, opt3);
            ps.setString(5, opt4);
            ps.setString(6, answer);
            ps.setString(7, questionId);
            
            int rowsUpdated = ps.executeUpdate();
            if (rowsUpdated > 0) {
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, "Successfully Updated");
            } else {
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, "No record found with the given ID");
            }
            
            ps.close();
            Con.close();
            
            setVisible(false);
            new updateQuestion().setVisible(true);
        } catch (Exception e) {
            e.printStackTrace();
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, "Failed to update record");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

