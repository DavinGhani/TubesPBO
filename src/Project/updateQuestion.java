    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String questionId = inputQuestionId.getText();
        try {
            Connection C = db_quizzapp.konfigDB();
            Statement statement = C.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            ResultSet result = statement.executeQuery("SELECT * FROM questions WHERE id='"+questionId+"'");
            if(result.first()){
                inputQuestion.setText(result.getString("name"));
                inputOption1.setText(result.getString("opt1"));
                inputOption2.setText(result.getString("opt2"));
                inputOption3.setText(result.getString("opt3"));
                inputOption4.setText(result.getString("opt4"));
                inputAnswer.setText(result.getString("answer"));
                
                inputQuestionId.setEditable(false);
            } else {
                JFrame jf = new JFrame();
                jf.setAlwaysOnTop(true);
                JOptionPane.showMessageDialog(jf, "Question Id doesn't Exist");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSearchActionPerformed

